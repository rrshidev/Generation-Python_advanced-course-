def fill_matrix(n_size, m_size):
    row = []
    for r in range(1, m_size + 1):
        row.append(r)

    matrix = [row]
    for r in range(n_size - 1):
        row = row[1:] + [row[0]]
        matrix.append(row)
    return matrix


def main():
    in_nums = [int(num) for num in input().split()]
    n, m = in_nums[0], in_nums[1]
    matrix = fill_matrix(n, m)
    for row in matrix:
        print(*row)


main()
